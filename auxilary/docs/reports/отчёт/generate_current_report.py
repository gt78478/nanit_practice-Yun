def patch_core_props(data: bytes) -> bytes:
    text = data.decode("utf-8", errors="ignore")
    text = re.sub(r"<dc:title>.*?</dc:title>", "<dc:title>ОТЧЕТ УП Юрченко - текущая версия Next.js</dc:title>", text)
    text = re.sub(r"<dc:subject>.*?</dc:subject>", "<dc:subject>Учебная практика</dc:subject>", text)
    return text.encode("utf-8")


def generate() -> None:
    with zipfile.ZipFile(TEMPLATE, "r") as zin:
        document = ET.fromstring(zin.read("word/document.xml"))
        body = document.find(qn("body"))
        if body is None:
            raise RuntimeError("word/document.xml has no w:body")
        sect_pr = body.find(qn("sectPr"))
        sect_copy = deepcopy(sect_pr) if sect_pr is not None else None
        for child in list(body):
            body.remove(child)
        for node in build_nodes():
            body.append(node)
        if sect_copy is not None:
            body.append(sect_copy)
        new_document_xml = ET.tostring(document, encoding="utf-8", xml_declaration=True)

        with zipfile.ZipFile(OUTPUT, "w", compression=zipfile.ZIP_DEFLATED) as zout:
            for item in zin.infolist():
                data = zin.read(item.filename)
                if item.filename == "word/document.xml":
                    data = new_document_xml
                elif item.filename == "docProps/core.xml":
                    data = patch_core_props(data)
                zout.writestr(item, data)

    print(OUTPUT)


