XML_NS = "http://www.w3.org/XML/1998/namespace"
ET.register_namespace("w", W_NS)


def qn(tag: str) -> str:
    return f"{{{W_NS}}}{tag}"


def el(tag: str, **attrs: str) -> ET.Element:
    return ET.Element(qn(tag), {qn(k): v for k, v in attrs.items()})


def add(parent: ET.Element, tag: str, **attrs: str) -> ET.Element:
    child = el(tag, **attrs)
    parent.append(child)
    return child


def para(text: str = "", style: str | None = None, *, bold: bool = False, italic: bool = False,
         align: str | None = None, size: int | None = None, page_break_before: bool = False) -> ET.Element:
    p = el("p")
    ppr = add(p, "pPr")
    if style:
        add(ppr, "pStyle", val=style)
    if align:
        add(ppr, "jc", val=align)
    if page_break_before:
        add(ppr, "pageBreakBefore")
    r = add(p, "r")
    rpr = add(r, "rPr")
    if bold:
        add(rpr, "b")
    if italic:
        add(rpr, "i")
    if size:
        add(rpr, "sz", val=str(size))
        add(rpr, "szCs", val=str(size))
    t = add(r, "t")
    t.set(f"{{{XML_NS}}}space", "preserve")
    t.text = text
    return p


def code_para(text: str) -> ET.Element:
    p = el("p")
    ppr = add(p, "pPr")
    ind = add(ppr, "ind", left="567")
    spacing = add(ppr, "spacing", before="0", after="0")
    r = add(p, "r")
    rpr = add(r, "rPr")
    add(rpr, "rFonts", ascii="Consolas", hAnsi="Consolas")
    add(rpr, "sz", val="18")
    add(rpr, "szCs", val="18")
    t = add(r, "t")
    t.set(f"{{{XML_NS}}}space", "preserve")
    t.text = text
    return p


def bullet(text: str) -> ET.Element:
    return para(f"• {text}")


def table(rows: list[list[str]]) -> ET.Element:
    tbl = el("tbl")
    tbl_pr = add(tbl, "tblPr")
    add(tbl_pr, "tblStyle", val="a3")
    add(tbl_pr, "tblW", w="0", type="auto")
    borders = add(tbl_pr, "tblBorders")
    for side in ("top", "left", "bottom", "right", "insideH", "insideV"):
        add(borders, side, val="single", sz="4", space="0", color="808080")

    for ridx, row in enumerate(rows):
        tr = add(tbl, "tr")
        for cell in row:
            tc = add(tr, "tc")
            tc_pr = add(tc, "tcPr")
            add(tc_pr, "tcW", w="2400", type="dxa")
            text_p = para(cell, bold=(ridx == 0))
            tc.append(text_p)
    return tbl


def read_snippet(path: str, start: str | None = None, max_lines: int = 26) -> list[str]:
    source = (ROOT / path).read_text(encoding="utf-8")
    lines = source.splitlines()
    if start:
        for idx, line in enumerate(lines):
            if start in line:
                lines = lines[idx: idx + max_lines]
                break
    else:
        lines = lines[:max_lines]
    return lines[:max_lines]


def section(title: str, level: int = 1, page_break_before: bool = False) -> ET.Element:
    return para(title, style=str(level), bold=True, page_break_before=page_break_before)


def listing(title: str, path: str, start: str | None = None, max_lines: int = 26) -> list[ET.Element]:
    nodes: list[ET.Element] = [para(title, bold=True)]
    for line in read_snippet(path, start, max_lines):
        nodes.append(code_para(line))
    return nodes


